static bool MIB_instance_mqxTaskEntry
   (
      RTCSMIB_WALK_PTR     mib,
      uint32_t              op,
      bool (_CODE_PTR_  find)(uint32_t, void *, void **),
      bool         *found,
      void           **instance
   )
{ /* Body */
   struct {
      uint32_t mqxTaskID;
   } index;

   *found = FALSE;
   *instance = NULL;

   switch (op) {

   case RTCSMIB_OP_GET:
   case RTCSMIB_OP_SET:

      if (mib->inlen == 0) break;
      if (!RTCSMIB_id_read32(mib, &index.mqxTaskID)) return FALSE;
      if (mib->inlen != 0) break;
      *found = find(op, &index, instance);
      break;

   case RTCSMIB_OP_GETNEXT:
      if (mib->inlen == 0) {
         index.mqxTaskID = 0;
      } else {
         if (!RTCSMIB_id_read32(mib, &index.mqxTaskID)) return FALSE;
         if(index.mqxTaskID < 0xffffffff) // 4294967295
            index.mqxTaskID++;
      } /* Endif */
      *found = find(op, &index, instance);
      break;

   case RTCSMIB_OP_GETFIRST:
      index.mqxTaskID = 0;
      *found = find(RTCSMIB_OP_GETNEXT, &index, instance);
      break;
   } /* Endswitch */

   if (*found && (op != RTCSMIB_OP_SET)) {
      if (!RTCSMIB_id_write32 (mib, index.mqxTaskID)) return FALSE;
   } /* Endif */

   return TRUE;
} /* Endbody */


