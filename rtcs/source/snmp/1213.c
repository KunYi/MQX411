static uint32_t MIB_set_dispstr
   (
      unsigned char * *dest,
      unsigned char       *varptr,
      uint32_t         varlen
   )
{ /* Body */
   unsigned char *dispstr = NULL;

   /* SIZE for DisplayString in MIB is between 0 to 255 */
   if ((varlen + 1) >= 255)
      return SNMP_ERROR_wrongLength;

   if (varlen) {
      dispstr = RTCS_mem_alloc(varlen+1);
      if (!dispstr) {
         return SNMP_ERROR_resourceUnavailable;
      } /* Endif */
      _mem_copy(varptr, dispstr, varlen);
      dispstr[varlen] = '\0';
   } /* Endif */

   if (*dest) {
      _mem_free(*dest);
   } /* Endif */

   *dest = dispstr;
   return SNMP_ERROR_noError;

} /* Endbody */

